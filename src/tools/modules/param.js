/**
 * @param {Object} json
 * @returns {Array}
 */
import cleanArray from './cleanArray'
export default function param(json) {
  if (!json) return ''
  return cleanArray(
    Object.keys(json).map(key => {
      if (json[key] === undefined) return ''
      return encodeURIComponent(key) + '=' + encodeURIComponent(json[key])
    })
  ).join('&')
}
